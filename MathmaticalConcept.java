package JavaTraining;

public class MathmaticalConcept {

	public static void main(String[] args) {
		
		int i = 10/2;
		System.out.println(i);
		
		int j = 9/2;
		System.out.println(j);
		
		System.out.println(11/2.0);
		
		float d = 9/2f;
		System.out.println(d);
		
		int k = 9/0;
		System.out.println(k);	
				

	}

}
